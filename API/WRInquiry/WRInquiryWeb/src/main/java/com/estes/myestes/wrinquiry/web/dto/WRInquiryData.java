package com.estes.myestes.wrinquiry.web.dto;

public class WRInquiryData {
	
	private String checkbox;
	private String proNumber;
	private String boNumber;
	private String poNumber;
	private String dateOfcreation;
	private String typeOfCreation;
	
	
	public String getCheckbox() {
		return checkbox;
	}
	public void setCheckbox(String checkbox) {
		this.checkbox = checkbox;
	}
	public String getProNumber() {
		return proNumber;
	}
	public void setProNumber(String proNumber) {
		this.proNumber = proNumber;
	}
	public String getBoNumber() {
		return boNumber;
	}
	public void setBoNumber(String boNumber) {
		this.boNumber = boNumber;
	}
	public String getPoNumber() {
		return poNumber;
	}
	public void setPoNumber(String poNumber) {
		this.poNumber = poNumber;
	}
	public String getDateOfcreation() {
		return dateOfcreation;
	}
	public void setDateOfcreation(String dateOfcreation) {
		this.dateOfcreation = dateOfcreation;
	}
	public String getTypeOfCreation() {
		return typeOfCreation;
	}
	public void setTypeOfCreation(String typeOfCreation) {
		this.typeOfCreation = typeOfCreation;
	}
	
	
	

}
